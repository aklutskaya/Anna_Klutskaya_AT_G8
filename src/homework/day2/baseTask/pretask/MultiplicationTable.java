package homework.day2.baseTask.pretask;

public class MultiplicationTable {

    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {
            System.out.println();
            for (int b = 1; b <= 5; b++) {
                System.out.println(i * b + " ");
            }
        }
    }
}
