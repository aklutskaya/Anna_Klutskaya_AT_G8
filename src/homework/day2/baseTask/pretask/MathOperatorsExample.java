package homework.day2.baseTask.pretask;

public class MathOperatorsExample {

    public static void main(String[] args) {

        int a = 8;
        int b = 16;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
