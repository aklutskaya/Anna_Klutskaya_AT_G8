package homework.day2.baseTask;

public class TrainMethodsReturn {

    public int returnNewInt(int number) {
        return number * 3;

    }

    public long returnNewLong(long number) {
        return number - 4;

    }

    public String returnNewChar(char inputChar) {

        return String.valueOf(inputChar) + inputChar;
    }

    public float returnNewFloat(float number) {

        return number / 2;
    }

    public double returnNewDouble(double number) {

        return number + 8;
    }

    //
    public int returnNewShort(int number) {
        return number - 1;
    }

    public int returnNewByte(int number) {
        return number * 2;
    }

    public boolean returnNewBoolean(boolean value) {
        return value;
    }

}
